-- Extends and enhances the regab_prng_gp table.
--

SET search_path TO reversi;

BEGIN;
 
INSERT INTO migrations (migration_id, ins_time, label, description)
VALUES (0102, now(), 'gp_ext', 'game position extentions and indexes');

CREATE INDEX regab_prng_gp_bes_idx ON regab_prng_gp (batch_id, empty_count, status);

--
-- The new field parent_gp_id could be NULL or has the reference to the parent game position.
--
-- Adds structure to regab_prng_gp table to host the new rows having the best next move played.
-- This doubles the solved positions for empty_count = empty_count - 1. 
--
ALTER TABLE regab_prng_gp ADD COLUMN parent_gp_id BIGINT DEFAULT NULL REFERENCES regab_prng_gp (seq) ON DELETE CASCADE;

--
-- Staus can take the following values:
--  - INS: Inserted.
--  - WIP: Selected for game_value calculation.
--  - CMP: game_value has been coputed and saved.
--  - CMQ: Marked for offspring generation.
--  - CMW: Selected anc locked for offspring generation.
--  - CMR: It is the offspring generated by a parent game position by playing the best move.
--  - CMS: Offspring has been generated.
--
ALTER TABLE regab_prng_gp ADD CONSTRAINT status_chk CHECK (status IN ('INS', 'WIP', 'CMP', 'CMQ', 'CMW', 'CMR', 'CMS'));

--
-- This index is needed to effectively delete records in regab_prng_gp.
--
CREATE INDEX regab_prng_gp_parent_gp_id_idx ON regab_prng_gp (parent_gp_id);

COMMIT;
